public class InfoSis {
    private int alunos;
    private int emails;
    private int turmas;
    private int cursos;

    public int getAlunos() {
        return alunos;
    }

    public void setAlunos(int alunos) {
        this.alunos = alunos;
    }

    public int getEmails() {
        return emails;
    }

    public void setEmails(int emails) {
        this.emails = emails;
    }

    public int getTurmas() {
        return turmas;
    }

    public void setTurmas(int turmas) {
        this.turmas = turmas;
    }

    public int getCursos() {
        return cursos;
    }

    public void setCursos(int cursos) {
        this.cursos = cursos;
    }
    
}
